{
    u16 i = 0;     
	
	for (i = 0; i < 18; i++);
}


void Delay(u16 time) //40us
{
    u16 i = 0,j=0;     
	
	for (i = 0; i < time; i++)
	{
		j=100;
	    while(j--);
	
	}
}

